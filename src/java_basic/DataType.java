package java_basic;

public class DataType {
    public static void main(String[] args) {
        //Two type of java data type
        //1. primitive byte/short/long/int/float/double/boolean/char
        //a.byte
        //a=variable/identifier

        byte a = 14;
        System.out.println(a);
        byte b = 12;
        System.out.println(b);


        //b.short
        //short c = 129;

        //c.int
        int e = 123456;
        System.out.println("primitive data int value = " + e);
        //d.long
        long i = 1234565689L;

        //e.float
        float t = 1.12233335566F;
        System.out.println(t);
        //. upto 07 digit count after decimal

        //f.double
        //real life used mostly. upto 16 digit count after decimal
        double m = 1.234567891112131415;
        System.out.println(m);

        //g.boolean
        boolean hi = false;
        System.out.println(hi);
        System.out.println("hi value = " + hi);


        //h.char
        //like %/#/@ etc. but it should be only single chracter
        char kite = '$';
        System.out.println(kite);
        char hobby = 'a';
        System.out.println(hobby);
        //2. non primitive
//class/array/String
        String x = "1234Tasikun";
        System.out.println(x);
        System.out.println("x value = " + x);




    }
}
